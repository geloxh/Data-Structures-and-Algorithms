public class Main {
    public static void main(String[] args)
    {

        // Initializing array
        int[] arr = { 1, 2, 3, 4, 5 };

        // Size of array
        int n = arr.length;

        // Traversing array
        for (int i = 0; i < n; i++)
            System.out.print(arr[i] + " ");
    } 
}